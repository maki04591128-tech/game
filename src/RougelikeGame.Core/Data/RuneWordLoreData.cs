            "tortima" => "暗黒のエネルギーが溢れ出し、空間を歪める…",
            "graeda" => "温かい光が傷口を包み込む。",
            "verja" => "魔力の膜が体を包み、盾のように固まる。",
            "hylja" => "光が歪み、姿がゆっくりと消え始める…",
            "binda" => "見えない鎖が空中に現れ、対象を縛り上げる。",
            "sofa" => "柔らかな眠りの波が広がり、意識が遠くなっていく…",
            "styra" => "暗い意思が相手の精神に侵入し始める。",
            "vekja" => "生命の輝きが宿り、死の淵から引き戻す…",
            _ => "魔力が指先に集まり、形を成す。"
        };

        if (elementWord != null)
        {
            var elementDesc = elementWord switch
            {
                "eldr" => "炎の属性が付加され、",
                "iss" => "氷の属性が付加され、",
                "myrkur" => "闇の属性が付加され、",
                "ljós" => "光の属性が付加され、",
                _ => ""
            };
            return elementDesc + baseText;
        }

        return baseText;
    }

