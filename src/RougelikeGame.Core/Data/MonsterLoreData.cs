    /// <summary>
    /// 図鑑登録用の5段階説明辞書を生成する
    /// </summary>
    public static Dictionary<int, string> GetLevelDescriptions(MonsterRace race)
    {
        var dict = new Dictionary<int, string>();
        for (int level = 1; level <= 5; level++)
        {
            dict[level] = GetDescription(race, level);
        }
        return dict;
    }

